package com.controlstatement;

public class DemoSwitch {

	public static void main(String[] args) {
		
		int number = 10;
		
		switch(number){
		
		case 10 : System.out.println("Your result is : "+10);
		break;
		
		case 2 : System.out.println("Your result is : "+20);
		break;
		
		default : System.out.println("Invalid");
		break;
		
		}

	}

}
